// js  object Data type
// An object is a data type that store data in terms of key - Values pairs

let person ={
    Name : "James Atione",
    Age : 54,
    isRegistered : true
};

console.log("The detials of the person is: ", person)

// To access the items of an object we have 2 methods
// metod 1 :  by use of square brackets;
console.log(person["Name"])
console.log(person["Age"])

// Method 2 : By use of the dot separator
console.log(person.isRegistered)
console.log(person.Name)

